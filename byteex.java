public class byteex {
    public static void main(String[] args) {
        byte smallno = 100;
        byte minvalue = -128;
        byte maxvalue = 127;
        byte result = (byte) (smallno + 20);

        system.out.println("Small number: " + smallno);
        system.out.println("Minimum byte value: " + minvalue);
        system.out.println("Maximum byte value: " + maxvalue);
        system.out.println("Result Number: " + result);

    }
}